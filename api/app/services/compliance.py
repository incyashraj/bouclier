def _build_generic_report(agents, events, date_from, date_to) -> dict:
    """Generic compliance report."""
    total_actions = len(events)
    violations = [e for e in events if e.outcome == "violation"]
    total_spend = sum(float(e.value_usd or 0) for e in events if e.outcome == "success")

    return {
        "report_type": "generic",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "report_period": {"from": date_from.isoformat(), "to": date_to.isoformat()},
        "summary": {
            "agents": len(agents),
            "actions": total_actions,
            "violations": len(violations),
            "spend_usd": f"{total_spend:.2f}",
        },
        "agents": [{"did": a.did, "status": a.status, "model": a.model} for a in agents],
        "events": [
            {
                "timestamp": e.timestamp.isoformat(),
                "agent_id": e.agent_id,
                "outcome": e.outcome,
                "value_usd": e.value_usd,
            }
            for e in events
        ],
    }


def _report_to_csv(report: dict) -> str:
    """Convert the action/event log from a report to CSV format."""
    output = io.StringIO()
    events = report.get("action_log") or report.get("transaction_log") or report.get("events", [])

    if not events:
        return "No events in report period\n"

    writer = csv.DictWriter(output, fieldnames=events[0].keys())
    writer.writeheader()
    writer.writerows(events)
    return output.getvalue()


def _report_to_pdf(report: dict) -> bytes:
    """Render report as a styled PDF document. Returns raw PDF bytes."""
    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=A4, topMargin=20 * mm, bottomMargin=15 * mm)
    styles = getSampleStyleSheet()
    story = []

    title_style = ParagraphStyle("Title", parent=styles["Title"], fontSize=16, textColor=colors.HexColor("#111111"))
    heading_style = ParagraphStyle("Heading", parent=styles["Heading2"], fontSize=12, spaceAfter=6)
    body_style = styles["BodyText"]

    # Title
    report_type = report.get("report_type", "Compliance Report")
    story.append(Paragraph(f"Bouclier — {report_type} Report", title_style))
    story.append(Spacer(1, 8 * mm))

    # Summary
    summary = report.get("summary", {})
    if summary:
        story.append(Paragraph("Summary", heading_style))
        for k, v in summary.items():
            story.append(Paragraph(f"<b>{k.replace('_', ' ').title()}</b>: {v}", body_style))
        story.append(Spacer(1, 5 * mm))

    # Period
    period = report.get("report_period", {})
    if period:
        story.append(Paragraph(f"Period: {period.get('from', '')} — {period.get('to', '')}", body_style))
        story.append(Spacer(1, 5 * mm))

    # Events table
    events = report.get("action_log") or report.get("transaction_log") or report.get("events", [])
    if events:
        story.append(Paragraph("Event Log", heading_style))
        cols = list(events[0].keys())[:6]
        header = [c.replace("_", " ").title() for c in cols]
        data = [header]
        for ev in events[:200]:
            data.append([str(ev.get(c, ""))[:30] for c in cols])

        table = Table(data, repeatRows=1)
        table.setStyle(TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#FF451A")),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
            ("FONTSIZE", (0, 0), (-1, -1), 7),
            ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
            ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, colors.HexColor("#F4F4F5")]),
        ]))
        story.append(table)
        story.append(Spacer(1, 5 * mm))

    # Violations summary
    violations = report.get("violations") or report.get("compliance_violations", [])
    if violations:
        story.append(Paragraph(f"Violations ({len(violations)})", heading_style))
        for v in violations[:50]:
            story.append(Paragraph(
                f"• {v.get('timestamp', '')} — {v.get('violation_type') or v.get('type', '')} "
                f"(Agent: {v.get('agent_id', 'N/A')})",
                body_style,
            ))

    # Footer
    story.append(Spacer(1, 10 * mm))
    story.append(Paragraph(
        f"Generated by Bouclier Protocol at {report.get('generated_at', '')}",
        ParagraphStyle("Footer", parent=body_style, fontSize=7, textColor=colors.grey),
    ))

    doc.build(story)
    return buf.getvalue()
